<div class="content copy tab-row clearfix">
    <div>
        <h2>            <a href="../industries/evaluation-criteria.php" title="Aerospace &amp; Aircraft">Aerospace & Aircraft</a>
</h2>
        <p>Essential to the aerospace &amp; aircraft industry, nitrogen is used in high Reynolds number wind tunnels, heat treating furnaces and autoclaves to help create incredibly strong but lightweight materials. Nitrogen is also used as an assist gas for laser cutting applications.</p>
    </div>
</div>

<div class="content copy tab-row clearfix">
    <div>
        <h2>            <a href="../industries/apply-for-scholarship.php" title="Automotive Fabrication">Automotive Fabrication</a>
</h2>
        <p>Assembly plants, tier 1s and tier 2s use nitrogen in combination with other welding gases to weld auto parts, frames, mufflers and other components because of its ability to provide the necessary atmosphere needed to produce adequate welds with any material. It&rsquo;s also an important safety component in airbags.</p>
    </div>
</div>

<div class="content copy tab-row clearfix">
    <div>
        <h2>            <a href="../industries/application-timeline.php" title="Chemicals">Chemicals</a>
</h2>
        <p>Used as a pressurizing gas, nitrogen can help propel liquids through pipelines. It is also used to shield oxygen-sensitive materials from the air and to remove volatile organic chemicals from process streams.</p>
    </div>
</div>

<div class="content copy tab-row clearfix">
    <div>
        <h2>            <a href="../industries/energy.php" title="Energy">Energy</a>
</h2>
        <p>As an inert industrial gas, nitrogen is used as a blanketing agent to separate sensitive products and processes from air. It is also used as a purging agent in piping and equipment to prevent contamination.</p>
    </div>
</div>

<div class="content copy tab-row clearfix">
    <div>
        <h2>            <a href="../industries/food-and-beverage.php" title="Food &amp; Beverage">Food & Beverage</a>
</h2>
        <p>Nitrogen is a key cryogenic agent in cooling, chilling and food freezing. Because of its extremely cold temperatures, immersion freezing in liquid nitrogen is the fastest freezing method known for producing Individually Quick Frozen (IQF) foods. Nitrogen also plays a key role in reducing spoilage, discoloration and off&ndash;flavors, giving strength to retail packaging.</p>
    </div>
