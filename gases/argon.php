<div class="content tab-row page-title clearfix">
    <h1>USES OF ARGON GAS AND LIQUID ARGON BY INDUSTRY</h1>

        <div class="page-tools">
            <ul>
                    <li class="share">
                        <a class="addthis_button" href="http://www.addthis.com/bookmark.php?v=300&amp;pubid=xa-5053907b15c86b81">Share</a>
