<div class="content copy tab-row clearfix">
    <div>
        <h2>            <a href="../industries/aerospace-and-aircraft.htm" title="Aerospace &amp; Aircraft">Aerospace & Aircraft</a>
</h2>
        <p>Argon is a versatile industrial gas used in welding applications, such as the welding of specialty alloys, and in the production of light bulbs and lasers. As an inert gas, it can also be used to provide an oxygen- and nitrogen- free environment for heat treating processes.</p>
    </div>
</div>

<div class="content copy tab-row clearfix">
    <div>
        <h2>            <a href="../industries/automotive-fabrication.htm" title="Automotive Fabrication">Automotive Fabrication</a>
</h2>
        <p>Argon is used in combination with other gases to weld auto parts, frames, mufflers and other automotive parts. It also provides an oxygen and nitrogen-free environment for annealing processes and is an essential component in airbags.</p>
    </div>
</div>

<div class="content copy tab-row clearfix">
    <div>
        <h2>            <a href="../industries/metal-production.htm" title="Metal Production">Metal Production</a>
</h2>
        <p>Used to maintain temperature and consistency of composition, argon prevents oxidation during processing. It also flushes out carbon monoxide and reduces chromium losses during the Argon-Oxygen Decarburization stainless steel refining process.</p>
    </div>
</div>

<div class="content copy tab-row clearfix">
    <div>
        <h2>            <a href="../industries/welding-and-metal-fabrication.htm" title="Welding &amp; Metal Fabrication">Welding & Metal Fabrication</a>
</h2>
        <p>Argon is used to create an inert shield gas to enhance arc stability and characteristics of gas metal arc welding. It is also the primary gas for the Gas Tungsten Arc Welding (GTAW) process.</p>
