                                            </a>
                                </h2>
                                <div class="content">
                                    <p>During manufacturing, nitrogen is used to inert the glass. This process prevents oxidation from occuring and allows the furnace to cool.</p>
                                </div>
                        </div>

                </div>
                <div class="block-grid-row clearfix">
                        <div class="gas item">
                                <h2>
                                            <a href="../gases/hydrogen.htm" title="Hydrogen">
