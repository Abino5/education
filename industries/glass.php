                                </h2>
                                <div class="content">
                                    <p>An industrial gas with the power to improve productivity, oxygen is used to enhance combustion in glass furnaces, reduce NOx emissions and improve quality.</p>
                                </div>
                        </div>
                        <div class="gas item">
                                <h2>
                                            <a href="../gases/nitrogen.htm" title="Nitrogen">
