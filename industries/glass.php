<div class="content copy tab-row clearfix">
    <div>
        <h2>Making glass clearly cleaner</h2>
        <p>The action of making glass is over 5,000 years old, but two decades ago Airtec made great strides in getting this ancient craft ready for the twenty-first century. With our full range of industrial gases, technologies and equipment, we successfully converted the first large container glass furnace to an oxy-fuel burner. This development was good news not just for our customers, but also for the planet. Oxy-fuel melting improves glass quality and helps you save money by burning less fuel more efficiently. It also drastically reduces NOx emissions to help you meet today&rsquo;s regulatory requirements. Our burners allow you to produce a glass so clear and efficient that it could only be centuries in the making.</p>
