        <div class="ind_gases-content block-grid">

                <div class="block-grid-row clearfix">
                        <div class="gas item">
                                <h2>
                                            <a href="../gases/nitrogen.htm" title="Nitrogen">
                                                <img src='../-/media/praxairus/images/1_0-gases/1_0-gases-overview-new/1_0_nitrogen_49x46.png' alt='Nitrogen' />
                                                <img src='../-/media/praxairus/images/1_0-gases/1_0-gases-overview-new/1_0_nitrogen_rollover_49x46.png' class='hover' alt='Nitrogen rollover' />
                                                <span>Nitrogen</span>
                                            </a>
                                </h2>
                                <div class="content">
                                    <p>Gaseous nitrogen is commonly used for blanketing &mdash; protecting a process from moisture intrusion, oxidation, degradation and contamination. Additional uses include purging, pressure transferring, mixing and capturing air emmisions. Cryogenic liquid nitrogen is used to control the temperature in reactor cooling applications and to preserve biological samples.</p>
                                </div>
                        </div>
                        <div class="gas item">
                                <h2>
                                            <a href="../gases/oxygen.htm" title="Oxygen">
                                                <img src='../-/media/praxairus/images/1_0-gases/1_0-gases-overview-new/1_0_oxygen_52x46.png' alt='Oxygen' />
                                                <img src='../-/media/praxairus/images/1_0-gases/1_0-gases-overview-new/1_0_oxygen_rollover_52x46.png' class='hover' alt='Oxygen rollover' />
                                                <span>Oxygen</span>
                                            </a>
                                </h2>
                                <div class="content">
                                    <p>Critical for cell growth applications, oxygen is used in fermenters and bioreactors.</p>
                                </div>
                        </div>

                </div>
                <div class="block-grid-row clearfix">
                        <div class="gas item">
                                <h2>
                                            <a href="../gases/carbon-dioxide.htm" title="Carbon Dioxide">
                                                <img src='../-/media/praxairus/images/1_0-gases/1_0-gases-overview-new/1_0_carbondioxide_80x46.png' alt='Carbon Dioxide' />
                                                <img src='../-/media/praxairus/images/1_0-gases/1_0-gases-overview-new/1_0_carbondioxide_rollover_80x46.png' class='hover' alt='Carbon Dioxide rollover' />
                                                <span>Carbon Dioxide</span>
                                            </a>
                                </h2>
                                <div class="content">
                                    <p>Carbon dioxide is used throughout the pharmaceutical market. As a gas, it is used in bench top incubators. In manufacturing, it is used in super critical fluid chromatography. And in wastewater treatment, it is used for self-buffered pH control.</p>
                                </div>
