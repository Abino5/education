        <h2>Medical Gases</h2>
        <p>At Airtec, we offer a full line of medical gases in high pressure and liquid cylinders. We currently supply all the hospitals under the Ministry of Health. We also have long term contracts to supply all the medical gases to the private clinics and hospitals.</p>
<h5>Medical Grade Oxygen</h5>
<p>We are a long time provider of medical grade oxygen and bulk oxygen storage equipment to medical facilities, for the treatment or prevention of insufficient oxygen in the blood (hypoxemia and hypoxia).</p>
<h5>Medical Air</h5>
<p>Medical air is used extensively in the ICU, PICU and NICU areas, and with pulmonary nebulizers to reduce the risk of excess oxygen in the lungs or other body tissue (hyperoxia) during mechanical ventilation or surgical procedures.</p>
<h5>Medical Nitrogen</h5>
<p>Medical nitrogen is often used in pre-flight lung testing to measure a person&rsquo;s response to a simulated aircraft cabin environment (hypoxic challenge testing) as well as to power medical devices, and as a cryogen to freeze and preserve blood, tissue, and other biological specimens, and to freeze and destroy diseased tissue in cryosurgery and dermatology.</p>
<h5>Medical Nitrous Oxide</h5>
<p>Medical nitrous oxide is used in the healthcare industry as an analgesic and adjunct to anesthesia.</p>
<h5>Medical Helium and Medical Heliox</h5>
<p>Mixtures containing helium are often used for the treatment of fixed partial upper obstruction or increased airway resistance, helping patients breathe easier. It is also used in liquefied form to help MRI machines reach a superconductance state in order to produce high-resolution body images without radiation exposure.</p>
<h5>Carbon Dioxide</h5>
<p>Used for insufflation, carbon dioxide can also be combined with oxygen or air for respiratory simulation.</p>
