							<h3 class="subsection-headline">Privacy</h3>

							<div class="inline-copy-headline-container">
								<h4 class="inline-copy-headline">Third-party cookie blocking.</h4>
								<p>The web pages you visit often leave cookies from third-party websites. These cookies can be used to track where you go on the web, target you with ads, or create a profile of your online activities. Safari was the first browser to block these cookies by default. And by default it also prevents third-party websites from leaving data in your cache, local storage, or databases.</p>
							</div>

							<div class="inline-copy-headline-container last-copy-element-vertical-spacing">
								<h4 class="inline-copy-headline">Private Browsing windows.</h4>
								<p>When you use Private Browsing, Safari doesn’t remember the pages you visit, your search history, or your AutoFill information. Each tab in a Private Browsing window is isolated from the others, so websites you view in one tab can’t see your cookies from websites in another tab. This helps prevent websites you’re logged in to from tracking you as you browse.</p>
							</div>

						</div>
						<div class="column large-5 large-push-1 gutter-half-left">
							<figure class="image-security-icon security-icon"></figure>
