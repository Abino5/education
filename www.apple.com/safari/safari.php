							<div class="inline-copy-headline-container">
								<h4 class="inline-copy-headline">Sandboxing for websites.</h4>
								<p>Sandboxing provides built-in protection against malicious code and malware by restricting what websites can do. And because Safari runs web pages in separate processes, any harmful code you come across in one page is confined to a single browser tab, so it can’t crash the whole browser or access your data.</p>
							</div>

							<div class="inline-copy-headline-container last-copy-element-vertical-spacing">
								<h4 class="inline-copy-headline">Protection from harmful sites.</h4>
								<p>Safari helps protect you against fraudulent Internet sites and those that harbor malware — before you visit them. If a website looks suspicious, Safari prevents it from loading and warns you.</p>
